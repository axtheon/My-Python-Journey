# A list can store any datatypes. we can print whatever value we want from a list or we can print the whole list
list = ["Apple","Orange","Abdullah","Saeed","Khan",5,False,True,38.0499]
print(list[0])
print(list[7])
# in strings we cant change anything because strings are immutable but if we want to change something we can use a list and lists are mutable
list[0] = "Grapes"
print(list[0])
# A list can be indexed just like a string 
print(list[1:4])